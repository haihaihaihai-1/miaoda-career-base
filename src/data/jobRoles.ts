export const getJobRoleById = (id: number): JobRole | undefined =>
  jobRoles.find((j) => j.id === id);

export const searchJobRoles = (query: string): JobRole[] => {
  if (!query.trim()) return jobRoles;
  const q = query.toLowerCase();
  return jobRoles.filter(
    (j) =>
      j.title.toLowerCase().includes(q) ||
      j.industry.toLowerCase().includes(q) ||
      j.major_group.toLowerCase().includes(q) ||
      j.skills_required.some((s) => s.toLowerCase().includes(q))
  );
};
